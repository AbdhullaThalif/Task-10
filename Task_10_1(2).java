package programming;

public class ConstructorClass {
	
	public static void main(String[] args) {
	    Person objper = new Person("Karan", 10);
	    System.out.println("Name: " + objper.getName());
	    System.out.println("Age : " + objper.getAge());
	  }

}

//  Output:
//  Name: Karan
//  Age : 10